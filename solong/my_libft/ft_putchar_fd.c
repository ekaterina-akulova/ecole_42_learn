#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	int	a;

	a = ft_atoi(&c);
	a = ft_isascii((int)c);
	if (a)
	{
		if (c >= 0 && c <= 127)
		{
			write(fd, &c, 1);
		}
	}
}	
